package concepts.variables;

public class Double {
    public static void main(String[] args) {
        int x = 10;
        int y = 4;
        int a = x / y;
        double b = x / y;
        double c = b / x;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
